public class numbers {
    public static void main(String[] args) {
        System.out.println(10);  //Decimal
        System.out.println(0124); //octal 
        System.out.println(0X234a ); //Hexa Decimal
        System.out.println(3e3); //3*10^3
        System.out.println(9/2);
        System.out.println((float)9/2);
        System.out.println(9.5+2);
    }
}
